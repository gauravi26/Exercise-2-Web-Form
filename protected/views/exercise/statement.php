<h2 style="color: rgb(255, 0, 0); font-family: 'Times New Roman', Times, serif; font-size: 24px;">A CLARION CALL TO <br>STOP HARMING KIDS IN <br>COVID RESPONSE</h2>

    <p>Much of the world’s response to Covid-19 has been in terms of <br>lockdowns, which have been hugely damaging by themselves,<br> especially to the poor and the working class. But the treatment <br>of children in the Covid response has been unconscionable. <br>We as parents, teachers, educationists, doctors, <br>epidemiologists, paediatricians, other professionals, and also as concerned citizens, call for an immediate end to ongoing disproportionate and hugely harmful disruptions to childrens’ lives.

The risk of Covid-19 to children is miniscule, far lesser than other risks we accept as normal part of life. Thus the disruptions to childrens’ lives are of no benefit to them, and are hugely harmful. Each of us adults have enjoyed a normal childhood of learning, play, socialization, arts and music and sports. Each child today deserves the same.<br>

We therefore call for immediate resumption of all schools and related activities for all kids. We also call for:

<p style="text-align: justify;"><strong>1. No disruptive social distancing for kids:</strong> This disruption is no longer acceptable, is of no benefit for children, and is hugely harmful to their mental and social well-being, and all-round development.</p>
<p><strong>2. No emergency-use jabs for kids:</strong> Children have not had a Covid emergency anywhere in the world; therefore use of emergency use medical intervention is unacceptable. Kids should not be given jabs whose long-term effects are clearly unknown.</p>
<p><strong>3. No asymptomatic Covid testing for kids:</strong> “Asymptomatic illness” is an oxymoron. Disruptions to schools or other kids’ activities based on such testing is no longer acceptable.</p>
The above measures are still being followed, with the stated goal of virus eradication, or Zero-Covid. Chasing Zero-Covid is not only an unachievable scientific fantasy, it is also hugely harmful to kids. This harm must stop immediately. We owe a normal, healthy, fear-free, anxiety-free environment to kids.</p>

